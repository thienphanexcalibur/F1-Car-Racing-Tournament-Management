    private void removeTeamBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_removeTeamBtnActionPerformed
        // TODO add your handling code here:
        int rowIndex = this.teamTable.getSelectedRow();
        Team selectedTeam = (Team) this.teamTable.getValueAt(rowIndex, 1);
        try {
            new TeamRacerDAO().removeTeam(selectedTeam);
            dispose();
            new TeamManageFrm().setVisible(true);
        } catch (SQLException ex) {
            Logger.getLogger(TeamManageFrm.class.getName()).log(Level.SEVERE, null, ex);
        }
        JOptionPane.showMessageDialog(this, "Success!");
    }//GEN-LAST:event_removeTeamBtnActionPerformed

