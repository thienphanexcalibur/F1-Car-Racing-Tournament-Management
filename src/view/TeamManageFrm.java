    public void showTeams() throws SQLException {
        ArrayList<Team> teams = new TeamRacerDAO().getTeams(tournament);
        DefaultTableModel table = (DefaultTableModel) this.teamTable.getModel();
        for (Team t : teams) {
            table.addRow(new Object[]{t.getID(), t, t.getDescription()});
        }
    }

